package practise;

public class Bus {
	public String busName;
	public String BusInitial;
	public String BusDestination;
	public String BusTimings;
	public String getBusName() {
		return busName;
	}
	public void setBusName(String busName) {
		this.busName = busName;
	}
	public String getBusInitial() {
		return BusInitial;
	}
	public void setBusInitial(String busInitial) {
		BusInitial = busInitial;
	}
	public String getBusDestination() {
		return BusDestination;
	}
	public void setBusDestination(String busDestination) {
		BusDestination = busDestination;
	}
	public String getBusTimings() {
		return BusTimings;
	}
	public void setBusTimings(String busTimings) {
		BusTimings = busTimings;
	}

}
